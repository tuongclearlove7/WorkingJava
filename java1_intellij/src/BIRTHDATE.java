public class BIRTHDATE {
    private int year;
    private int month;
    private int day;

    public BIRTHDATE(int newyear, int newmonth, int newday) {
        year = newyear;
        month = newmonth;
        day = newday;
    }

    public void setYear(int newyear) {
        year = newyear;
    }
}
